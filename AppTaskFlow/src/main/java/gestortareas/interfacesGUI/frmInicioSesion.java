    private void iniciarSesion(){
        // OBTENER los valores ANTES de cualquier procesamiento
        String username = jTextUsuario.getText().trim();
        char[] passwordChars = jPasswordField.getPassword();
        String password = new String(passwordChars).trim();

        jPasswordField.setText("");

        java.util.Arrays.fill(passwordChars, ' ');

        controller.iniciarSesion(username, password);
    }

    private void abrirRegistro(){
        controller.abrirRegistro();
    }

